


//Caracteres especiales

public class CaracteresEspeciales {
    public static void main(String args[]) {
        System.out.println("Nueva Linea \n" + "ilde");  //Nueva linea
        System.out.println("Tabulador \t" + "ilde");  //Tabulador
        System.out.println("Retorno de Carro \r" + "ilde");  //Retorno de carro
        System.out.println("Barra invertida \\" + "ilde");  //Barra invertida
        System.out.println("Comilla simple \'" + "ilde");  //Comilla simple
        System.out.println("Comilla doble \"" + "ilde");  //Comilla doble
        System.out.println("Comilla simple \'" + "ilde");  //Comilla simple
        System.out.println("Retroceso \b" + "ilde");  //Retroceso
    }
}
